type I18nContextValue = {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: 'nav' | 'landing' | 'login' | 'app' | 'auth') => any
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  const initialLocale = (localStorage.getItem(STORAGE_KEY) as Locale) || 'fr'
  const [locale, setLocaleState] = useState<Locale>(initialLocale in translations ? initialLocale : 'fr')

  const setLocale = (value: Locale) => {
    setLocaleState(value)
    localStorage.setItem(STORAGE_KEY, value)
  }

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key) => (translations[locale] as any)[key],
    }),
    [locale],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used inside I18nProvider')
  return ctx
}
